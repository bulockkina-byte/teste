export function findTemplate(documentName: string): DocumentTemplate | null {
  const normalized = documentName
    .normalize('NFD')
    .replace(/[\u0300-\u036f]/g, '')
    .toUpperCase()
    .trim();
  return DOCUMENT_TEMPLATES.find(t => normalized.includes(t.name_pattern)) || null;
}
